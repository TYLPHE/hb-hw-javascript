function count_melons(melonArr) {
  const melonCounts = {};
  
  for (melon of melonArr) {
    if (melonCounts.hasOwnProperty(melon)) {
      melonCounts[melon] += 1
    } else {
      melonCounts[melon] = 1
    }
  }
  return melonCounts
}

